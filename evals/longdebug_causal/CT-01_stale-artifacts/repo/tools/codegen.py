"""Generate the User model and demo fixtures from schema/user.yaml.

Run it through `make generate` after touching the schema. The outputs
(generated/models.py and tests/fixtures/users.json) are derived, so don't
edit them by hand.
"""

from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
SCHEMA = ROOT / "schema" / "user.yaml"
MODELS = ROOT / "generated" / "models.py"
FIXTURES = ROOT / "tests" / "fixtures" / "users.json"

# The people behind the demo accounts. Stable source data; the schema
# decides which of these attributes actually ship.
PEOPLE = [
    {"id": 1, "email": "ada@example.com", "first_name": "Ada", "last_name": "Lovelace"},
    {"id": 2, "email": "alan@example.com", "first_name": "Alan", "last_name": "Turing"},
    {"id": 3, "email": "grace@example.com", "first_name": "Grace", "last_name": "Hopper"},
]

_PY_TYPES = {"int": "int", "str": "str", "bool": "bool", "float": "float"}


def _coerce(value: str):
    if value in ('""', "''", ""):
        return ""
    if value == "true":
        return True
    if value == "false":
        return False
    if value[:1] in "\"'" and value[-1:] == value[:1]:
        return value[1:-1]
    return value


def load_schema(path: Path = SCHEMA) -> list[dict]:
    fields: list[dict] = []
    for raw in path.read_text().splitlines():
        stripped = raw.strip()
        if not stripped or stripped.startswith("#"):
            continue
        if stripped.startswith("- "):
            fields.append({})
            stripped = stripped[2:].strip()
        if not fields or ":" not in stripped:
            continue
        key, _, value = stripped.partition(":")
        fields[-1][key.strip()] = _coerce(value.strip())
    return fields


def _value_for(field: dict, person: dict):
    name = field["name"]
    if name == "display_name":
        return f"{person['first_name']} {person['last_name']}"
    if name in person:
        return person[name]
    return field.get("default", "")


def build_users(fields: list[dict]) -> list[dict]:
    return [{f["name"]: _value_for(f, p) for f in fields} for p in PEOPLE]


def fixtures_json(fields: list[dict]) -> str:
    return json.dumps(build_users(fields), indent=2) + "\n"


def models_source(fields: list[dict]) -> str:
    lines = [
        "# Generated by tools/codegen.py - do not edit by hand.",
        "from dataclasses import dataclass",
        "",
        "",
        "@dataclass",
        "class User:",
    ]
    for f in fields:
        pytype = _PY_TYPES.get(f.get("type", "str"), "str")
        if f.get("optional"):
            lines.append(f"    {f['name']}: {pytype} = {f.get('default', '')!r}")
        else:
            lines.append(f"    {f['name']}: {pytype}")
    return "\n".join(lines) + "\n"


def generate() -> None:
    fields = load_schema()
    MODELS.parent.mkdir(parents=True, exist_ok=True)
    FIXTURES.parent.mkdir(parents=True, exist_ok=True)
    MODELS.write_text(models_source(fields))
    FIXTURES.write_text(fixtures_json(fields))


if __name__ == "__main__":
    generate()
    print("generated models + fixtures")
